const solve = (lines: string[][]) => {
  let sum = 0;

  for (let i = 0; i< lines.length; i++) {
    const [first, ...rest] = lines[i]
    const repeatedLetter = first.split('').find(letter => 
      rest.every(other => other.includes(letter))
    )

    const code = repeatedLetter.charCodeAt(0)
        if (code > 95) {
          sum += code - 96
          continue;
        }

        sum += code-64+26
        continue;
  }

  return sum;
};

export const first = (inputString: string) => {
  const lines = inputString
  .split('\n')
  .map(line => ([line.slice(0, line.length / 2), line.slice(line.length / 2)]));

  return solve(lines)
};

export const second = (inputString: string) => {
  const lines: string[][] = inputString
  .split('\n')
  .reduce((acc, curr) => {
    if (!(acc[acc.length - 1]?.length < 3)) {
      acc.push([])
    }
    acc[acc.length - 1].push(curr)

    return acc
  }, [])

  return solve(lines)
};

